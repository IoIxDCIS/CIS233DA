function getCity(cityName) {
    switch(cityName) {
        case "arizona": 
            document.body.id = "arizona";
            break;
        case "phoenix": 
            document.body.id = "phoenix";
            break;
        case "mesa": 
            document.body.id = "mesa";
            break;
        case "yuma": 
            document.body.id = "yuma";
            break;
        case "bisbee": 
            document.body.id = "bisbee";
            break;
        case "tuscon":
            document.body.id = "tuscon";
            break;
    }
}